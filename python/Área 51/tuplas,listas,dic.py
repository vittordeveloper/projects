resumo = """
# Listas, Dicionários e Tuplas em Python

- **Listas (`list`)**: Estruturas mutáveis, usadas para armazenar vários valores em uma única variável.  
  Exemplo: `numeros = [1, 2, 3, 4]`  
  Pode-se adicionar (`append`), remover (`remove`), ordenar (`sort`), entre outras operações.

- **Tuplas (`tuple`)**: Semelhantes às listas, mas **imutáveis** (não podem ser modificadas após a criação).  
  Exemplo: `dias = ("Segunda", "Terça", "Quarta")`  
  São úteis para armazenar valores que não devem mudar.

- **Dicionários (`dict`)**: Estruturas que armazenam pares **chave: valor**.  
  Exemplo: `pessoa = {"nome": "Brenno", "idade": 20}`  
  Permitem acesso rápido aos valores pelas chaves (`pessoa["nome"]` retorna `"Brenno"`).

"""
